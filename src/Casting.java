public class Casting {
    public static void main(String[] args) {
        double x = 2.1;
        double y = 5.8;
        int a = 2;
        int b = 5;
        // sin cast
        System.out.println(Math.ceil(x));
        // con cast
        System.out.println((int) Math.ceil(x));
        System.out.println( (double) a / b);
    }
}
